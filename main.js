import "./style.css";

document.getElementById("btn-menu").addEventListener("click", (ev) => {
  ev.preventDefault();
  const navContainer = document.getElementById("container");
  navContainer.classList.add("active");
});

document.getElementById("btn-close").addEventListener("click", (ev) => {
  ev.preventDefault();
  const navContainer = document.getElementById("container");
  navContainer.classList.remove("active");
});

